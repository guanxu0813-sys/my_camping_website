def guide_page_config(
    slug: str,
    title: str,
    description: str,
    priority: str = "0.7",
) -> dict:
    path = "/guides/" if slug == "index" else f"/guides/{slug}.html"
    file = "guides/index.html" if slug == "index" else f"guides/{slug}.html"
    return {
        "path": path,
        "file": file,
        "title": title,
        "description": description,
        "changefreq": "monthly",
        "priority": priority,
        "schema": "generated-guide",
    }


