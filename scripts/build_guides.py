def page_shell(
    *,
    title: str,
    description: str,
    canonical: str,
    schema: dict,
    current_path: str,
    body: str,
    b: ModuleType,
) -> str:
    return (
        "<!DOCTYPE html>\n"
        '<html lang="en">\n'
        f"{b.static_page_head(title, description, canonical, schema)}\n"
        '<body data-page="guide">\n'
        f"{b.static_header(current_path)}\n"
        f"{body}\n"
        f"{b.static_footer()}\n"
        f"{GENERATED_MARKER}\n"
        "</body>\n"
        "</html>\n"
    )


def product_name(product: dict, brands: dict[str, dict], b: ModuleType) -> str:
    brand_id = product.get("brandId", "")
    return (
        f"{b.brand_display_name(brands.get(brand_id), brand_id)} "
        f"{product.get('model', '')}"
    ).strip()


def comparison_value(product: dict, key: str, b: ModuleType) -> str:
    if key == "weight":
        return b.format_weight(product) or "Not listed"
    if key == "price":
        return b.format_price(product)
    if key == "capacity":
        return b.format_capacity_display(product) or "Not listed"
    return b.spec_value(product, key) or "Not listed"


