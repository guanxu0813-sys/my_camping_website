def guides_index_page(
    pages: list[dict],
    site_url: str,
    b: ModuleType,
) -> tuple[dict, str]:
    title = "Camping Gear Guides & Data Reports | CampGear Compare"
    description = (
        "Data-backed camping gear comparisons, lightweight shortlists and "
        "original weight and price reports using official product specs."
    )
    page = guide_page_config("index", title, description, "0.8")
    links = "".join(
        '<section class="static-panel">'
        f'<h2><a href="{b.escape_html(item["path"])}">{b.escape_html(item["title"])}</a></h2>'
        f'<p>{b.escape_html(item["description"])}</p>'
        "</section>"
        for item in pages
    )
    body = (
        '<main class="page page--sub static-page">\n'
        '  <nav class="static-breadcrumb" aria-label="Breadcrumb">\n'
        '    <a href="/index.html">Home</a><span aria-hidden="true">/</span>'
        "    <span>Guides</span>\n"
        "  </nav>\n"
        '  <header class="page__head">\n'
        '    <p class="page__eyebrow">Official specs, explained</p>\n'
        '    <h1 class="page__title">Camping Gear Guides &amp; Data Reports</h1>\n'
        "    <p class=\"page__lead\">Focused comparisons and original catalog "
        "analysis. These pages organize official specifications; they do not "
        "claim hands-on testing.</p>\n"
        "  </header>\n"
        f"{links}\n"
        "</main>"
    )
    schema = {
        "@context": "https://schema.org",
        "@type": "CollectionPage",
        "name": title,
        "description": description,
        "url": f"{site_url}/guides/",
        "mainEntity": {
            "@type": "ItemList",
            "numberOfItems": len(pages),
            "itemListElement": [
                {
                    "@type": "ListItem",
                    "position": index,
                    "name": item["title"],
                    "url": f"{site_url}{item['path']}",
                }
                for index, item in enumerate(pages, start=1)
            ],
        },
    }
    html = page_shell(
        title=title,
        description=description,
        canonical=f"{site_url}/guides/",
        schema=schema,
        current_path="",
        body=body,
        b=b,
    )
    return page, html


def build_guides(
    site_url: str,
    products: list[dict],
    brands: dict[str, dict],
    b: ModuleType,
) -> list[dict]:
    GUIDE_DIR.mkdir(exist_ok=True)
    product_map = {product.get("id"): product for product in products}
    pages: list[dict] = []

    for guide in COMPARISON_GUIDES:
        missing = [pid for pid in guide["product_ids"] if pid not in product_map]
        if missing:
            raise ValueError(f"{guide['slug']}: missing product IDs: {missing}")
        page = guide_page_config(
            guide["slug"], guide["title"], guide["description"]
        )
        (GUIDE_DIR / f"{guide['slug']}.html").write_text(
            comparison_page(guide, product_map, brands, site_url, b),
            encoding="utf-8",
        )
        pages.append(page)

    missing_tents = [pid for pid in LIGHTWEIGHT_TENT_IDS if pid not in product_map]
    if missing_tents:
        raise ValueError(f"lightweight tent guide: missing product IDs: {missing_tents}")
    page, html = lightweight_tents_page(product_map, brands, site_url, b)
    (GUIDE_DIR / "2-person-backpacking-tents-under-2kg.html").write_text(
        html, encoding="utf-8"
    )
    pages.append(page)

    page, html = data_report_page(products, brands, site_url, b)
    (GUIDE_DIR / "camping-gear-weight-price-report-2026.html").write_text(
        html, encoding="utf-8"
    )
    pages.append(page)

    existing_page = guide_page_config(
        "snow-peak-amenity-dome-2-vs-naturehike-cloud-up-2",
        "Snow Peak Amenity Dome 2 vs Naturehike Cloud Up 2",
        "A 2-person car-camping tent compared with a lighter budget backpacking tent.",
    )
    index_page, index_html = guides_index_page(
        [existing_page, *pages], site_url, b
    )
    (GUIDE_DIR / "index.html").write_text(index_html, encoding="utf-8")
    return [index_page, *pages]


