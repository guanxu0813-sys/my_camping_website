def related_products(product: dict, products: list[dict], brands: dict[str, dict], limit: int = 6) -> list[dict]:
    category = product.get("category")
    brand_id = product.get("brandId")
    current_id = product.get("id")
    same_brand = [
        p
        for p in products
        if p.get("id") != current_id and p.get("brandId") == brand_id and p.get("category") == category
    ]
    same_category = [
        p
        for p in products
        if p.get("id") != current_id and p.get("category") == category and p not in same_brand
    ]

    def sort_key(p: dict) -> tuple:
        b = brands.get(p.get("brandId", ""))
        return (brand_rank(b), (p.get("model") or "").lower())

    return sorted(same_brand, key=sort_key)[:limit] + sorted(same_category, key=sort_key)[: max(0, limit - len(same_brand))]


