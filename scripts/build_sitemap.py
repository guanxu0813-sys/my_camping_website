def product_primary_specs(product: dict) -> list[tuple[str, str]]:
    category = product.get("category")
    rows: list[tuple[str, str]] = []
    weight = format_weight(product)
    if weight:
        rows.append(("Weight", weight))
    capacity = format_capacity_display(product) or spec_value(product, "capacity")
    if capacity:
        rows.append(("Capacity", capacity))
    if category == "tent":
        for label, key in (("Structure", "structure"), ("Fabric / Waterproofing", "fabric")):
            value = spec_value(product, key)
            if value:
                rows.append((label, value))
    elif category == "tarp":
        for label, key in (("Type", "tarpType"), ("Size", "size")):
            value = spec_value(product, key)
            if value:
                rows.append((label, value))
    elif category == "sleeping-bag":
        for label, key in (("Type", "bagType"), ("Comfort Rating", "comfortTemp"), ("Fill", "fillType")):
            value = spec_value(product, key)
            if value:
                rows.append((label, value))
    elif category == "sleeping-pad":
        for label, key in (("Type", "padType"), ("R-Value", "rValue"), ("Size", "size"), ("Thickness", "thickness")):
            value = spec_value(product, key)
            if value:
                rows.append((label, value))
    elif category in {"table", "chair"}:
        for label, key in (("Type", "subcategory"), ("Seat Height", "seatHeight"), ("Packed Size", "foldedSize")):
            value = spec_value(product, key)
            if value:
                rows.append((label, value))
    rows.append(("Reference Price", format_price(product)))
    return rows


