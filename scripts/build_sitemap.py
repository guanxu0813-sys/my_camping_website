def localize_display_value(value: object) -> str:
    text = str(value or "")
    if not text:
        return ""
    for zh, en in DISPLAY_TERM_MAP:
        text = text.replace(zh, en)
    text = re.sub(r"(\d[\d\-–~to]*)\s*人", lambda m: f"{m.group(1)}-person", text)
