def clean_generated_html(directory: Path) -> None:
    if not directory.exists():
        return
    for path in directory.glob("*.html"):
        try:
            text = path.read_text(encoding="utf-8")
        except UnicodeDecodeError:
            continue
        if GENERATED_MARKER in text:
            path.unlink()


def sorted_visible_products(products: list[dict], brands: dict[str, dict]) -> list[dict]:
    def sort_key(product: dict) -> tuple:
        brand = brands.get(product.get("brandId", ""))
        return (brand_rank(brand), product.get("category", ""), (product.get("model") or "").lower())

    return sorted(products, key=sort_key)


def write_static_pages(site_url: str, products: list[dict], brands: dict[str, dict]) -> list[dict]:
    PRODUCT_DIR.mkdir(exist_ok=True)
    BRAND_DIR.mkdir(exist_ok=True)
    clean_generated_html(PRODUCT_DIR)
    clean_generated_html(BRAND_DIR)

    entries: list[dict] = []
    rows = sorted_visible_products(products, brands)
    affiliates = load_affiliates()
    affiliate_links = load_affiliate_links()
    stats = build_category_stats(rows)
    for product in rows:
        path = PRODUCT_DIR / f"{product.get('id')}.html"
        path.write_text(
            product_page_html(product, rows, site_url, brands, affiliates, affiliate_links, stats),
            encoding="utf-8",
        )
        entries.append(
            {
                "loc": f"{site_url}{product_path(product)}",
                "lastmod": source_lastmod_for_product(product),
                "changefreq": "monthly",
                "priority": "0.6",
                "group": "products",
            }
        )

    grouped: dict[tuple[str, str], list[dict]] = defaultdict(list)
    for product in rows:
        brand_id = product.get("brandId")
        category = product.get("category")
        if brand_id and category:
            grouped[(brand_id, category)].append(product)

    for (brand_id, category), group in sorted(
        grouped.items(),
        key=lambda item: (brand_rank(brands.get(item[0][0])), category_label(item[0][1])),
    ):
        if len(group) < MIN_BRAND_CATEGORY_PRODUCTS:
            continue
        path = BRAND_DIR / f"{brand_id}-{category}.html"
        group_sorted = sorted(group, key=lambda p: (p.get("model") or "").lower())
        path.write_text(
            brand_page_html(brand_id, category, group_sorted, rows, site_url, brands),
            encoding="utf-8",
        )
        lastmod = max(source_lastmod_for_product(product) for product in group_sorted)
        entries.append(
            {
                "loc": f"{site_url}{brand_category_path(brand_id, category)}",
                "lastmod": lastmod,
                "changefreq": "weekly",
                "priority": "0.7",
                "group": "brands",
            }
        )

    return entries


def render_url_entry(entry: dict) -> str:
    return (
        "  <url>\n"
        f"    <loc>{entry['loc']}</loc>\n"
        f"    <lastmod>{entry['lastmod']}</lastmod>\n"
        f"    <changefreq>{entry['changefreq']}</changefreq>\n"
        f"    <priority>{entry['priority']}</priority>\n"
        "  </url>"
    )


def write_urlset(path: Path, entries: list[dict]) -> None:
    body = "\n".join(render_url_entry(e) for e in entries)
    path.write_text(
        '<?xml version="1.0" encoding="UTF-8"?>\n'
        '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n'
        + body
        + "\n</urlset>\n",
        encoding="utf-8",
    )


def write_sitemap(site_url: str, pages: list[dict], products: list[dict], generated_entries: list[dict]) -> None:
    core_entries = [
        {
            "loc": f"{site_url}{page['path']}",
            "lastmod": page_lastmod(page, products),
            "changefreq": page["changefreq"],
            "priority": page["priority"],
        }
        for page in pages
    ]
    brand_entries = [e for e in generated_entries if e.get("group") == "brands"]
    product_entries = [e for e in generated_entries if e.get("group") == "products"]

    # Split sitemaps so GSC reports indexing coverage per page type.
    sections = [
        ("sitemap-core.xml", core_entries),
        ("sitemap-brands.xml", brand_entries),
        ("sitemap-products.xml", product_entries),
    ]
    index_items = []
    for filename, entries in sections:
        if not entries:
            continue
        write_urlset(ROOT / filename, entries)
        lastmod = max(e["lastmod"] for e in entries)
        index_items.append(
            "  <sitemap>\n"
            f"    <loc>{site_url}/{filename}</loc>\n"
            f"    <lastmod>{lastmod}</lastmod>\n"
            "  </sitemap>"
        )
    (ROOT / "sitemap.xml").write_text(
        '<?xml version="1.0" encoding="UTF-8"?>\n'
        '<sitemapindex xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n'
        + "\n".join(index_items)
        + "\n</sitemapindex>\n",
        encoding="utf-8",
    )


