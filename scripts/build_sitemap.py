def build_crawl_links(
    page: dict,
    products: list[dict],
    brands: dict[str, dict],
    seo: dict,
) -> str:
    if page.get("schema") == "home":
        links = [
            ('/tent.html', "Tent comparison"),
            ('/guides/', "Gear guides"),
            ('/guides/camping-gear-weight-price-report-2026.html', "2026 weight & price report"),
            ('/sleeping-pad.html', "Sleeping pad comparison"),
            ('/sleeping-bag.html', "Sleeping bag comparison"),
            ('/tarp.html', "Tarp comparison"),
            ('/furniture.html', "Camp furniture comparison"),
            ('/stove.html', "Stove comparison"),
            ('/backpack.html', "Backpack comparison"),
        ]
        return (
            '<p class="seo-crawl__links"><strong>Popular comparison hubs:</strong> '
            + " · ".join(
                f'<a href="{escape_html(path)}">{escape_html(label)}</a>' for path, label in links
            )
            + "</p>"
        )

    rows = sorted_products_for_page(page, products, brands)
    if not rows:
        return ""

    product_links = []
    for product in rows[:12]:
        brand_id = product.get("brandId", "")
        label = f"{brand_display_name(brands.get(brand_id), brand_id)} {product.get('model', '')}".strip()
        product_links.append((product_path(product), label))

    brand_links = []
    brand_category_counts: dict[tuple[str, str], int] = defaultdict(int)
    for product in rows:
        brand_id = product.get("brandId")
        category = product.get("category")
        if brand_id and category:
            brand_category_counts[(brand_id, category)] += 1

    seen: set[tuple[str, str]] = set()
    for product in rows:
        brand_id = product.get("brandId")
        category = product.get("category")
        key = (brand_id, category)
        if (
            not brand_id
            or not category
            or key in seen
            or brand_category_counts[key] < MIN_BRAND_CATEGORY_PRODUCTS
        ):
            continue
        seen.add(key)
        label = f"{brand_display_name(brands.get(brand_id), brand_id)} {category_label(category).lower()}"
        brand_links.append((brand_category_path(brand_id, category), label))
        if len(brand_links) >= 8:
            break

    blocks = []
    if product_links:
        blocks.append(
            '<p class="seo-crawl__links"><strong>Popular models:</strong> '
            + " · ".join(
                f'<a href="{escape_html(path)}">{escape_html(label)}</a>'
                for path, label in product_links
            )
            + "</p>"
        )
    if brand_links:
        blocks.append(
            '<p class="seo-crawl__links"><strong>Brand pages:</strong> '
            + " · ".join(
                f'<a href="{escape_html(path)}">{escape_html(label)}</a>'
                for path, label in brand_links
            )
            + "</p>"
        )
    blocks.append(
        '<p class="seo-crawl__links"><strong>Research guides:</strong> '
        '<a href="/guides/">Browse data-backed comparisons and reports</a></p>'
    )
    return "\n  ".join(blocks)


