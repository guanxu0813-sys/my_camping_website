def build_json_ld(
    page: dict,
    site_url: str,
    seo: dict,
    products: list[dict],
    brands: dict[str, dict],
) -> dict:
    schema = page.get("schema", "")
    if schema == "home":
        return build_home_schema(site_url, seo)
    if schema == "category":
        return build_category_schema(page, site_url, seo, products, brands)
    return build_legal_schema(page, site_url, seo)


