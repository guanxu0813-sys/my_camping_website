def first_value(*values: object) -> str:
    for value in values:
        if value is not None and value != "":
            return str(value)
    return ""


def spec_value(product: dict, key: str) -> str:
    specs = product.get("specs") or {}
    return first_value(product.get(key), specs.get(key))


def format_price(product: dict) -> str:
    currency = product.get("currency") or "USD"
    price_min = product.get("priceMin", product.get("price"))
    price_max = product.get("priceMax", product.get("price"))
    if price_min is None and price_max is None:
        return "Price not listed"
    suffix = f" {currency}"
    if price_min is not None and price_max is not None and price_min != price_max:
        return f"{price_min:g}–{price_max:g}{suffix}"
    price = price_min if price_min is not None else price_max
    return f"{price:g}{suffix}" if isinstance(price, (int, float)) else f"{price}{suffix}"


def format_weight(product: dict) -> str:
    weight_display = spec_value(product, "weightDisplay") or spec_value(product, "weightRange")
    if weight_display:
        return weight_display
    weight_kg = spec_value(product, "weightKg")
    return f"~{weight_kg} kg" if weight_kg else ""


def format_capacity_display(product: dict) -> str:
    raw = spec_value(product, "capacity")
    if raw:
        text = str(raw).strip()
        text = re.sub(r"-tent$", "", text, flags=re.IGNORECASE)
        text = re.sub(r"\bppl\b", "person", text, flags=re.IGNORECASE)
        person_match = re.search(r"(\d+)\s*[-–]\s*(\d+)\s*人", text)
        if person_match:
            return f"{person_match.group(2)}-person"
        single_match = re.search(r"(\d+)\s*人", text)
        if single_match:
            return f"{single_match.group(1)}-person"
        person_range = re.search(r"(\d+)\s*[-–]\s*(\d+)\s*person", text, flags=re.IGNORECASE)
        if person_range:
            return f"{person_range.group(2)}-person"
        single_person = re.search(r"(\d+)\s*person", text, flags=re.IGNORECASE)
        if single_person:
            return f"{single_person.group(1)}-person"
        return text
    model = str(product.get("model") or "")
    for pattern in (
        r"(?:dome|cabin|shelter|tent)\s+(\d+)",
        r"\b(\d+)\s*person",
        r"#(\d+)\b",
    ):
        match = re.search(pattern, model, flags=re.IGNORECASE)
        if match:
            return f"{match.group(1)}-person"
    return ""


